#include "../my_libft/libft.h"
#include "../includes/fdf.h"

void	free_map(t_map *map)
{
	int	i;

	i = 0;
	while (map->grid[i])
	{
		free(map->grid[i]);
		i++;
	}
	free(map->grid);
	free(map);
}
